public class Position {
    private int x_coOrdinate, y_coOrdinate;

    public Position(int x_coOrdinate, int y_coOrdinate) {
        this.x_coOrdinate = x_coOrdinate;
        this.y_coOrdinate = y_coOrdinate;
    }

    int getX_coOrdinate() {
        return x_coOrdinate;
    }

    int getY_coOrdinate() {
        return y_coOrdinate;
    }
}